void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	long i;

	i = nb;
	if (i< 0)
	{
		i=-i;
		ft_putnbr(i);
	}
	if (nb > 9)
	{
		ft_putnbr(i / 10);
		ft_putchar(i % 10 + 48);
	}
	if (nb <= 9)
	{
		ft_putchar(i % 10 + 48);
	}
}
