int		ft_is_prime(int nb)
{
	int i;
	int a;

	i = 2;
	a = nb / 2;
	if (nb <= 1)
		return (0);
	while (i <= a && nb < 2147483647)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
